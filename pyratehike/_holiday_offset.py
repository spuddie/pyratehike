def _holiday_offset(calendar: str, datum: "_RHDate") -> Optional[int]:
    """Private function to calculate the holiday offset.

    The holiday offset is the jump from a holiday to the next business
    day. For non-holidays None is returned. For holidays the jump
    takes into account weekends; but non-holiday weekend days still
    return None and should be checked separately.

    Parameters
    ----------
    calendar : str
        which calendar to use; "TARGET" or "UK"
    datum : _RHDate
        the date for which to calculate the offset

    Returns
    -------
    int or None
        the holiday offset for a holiday, None for a non-holiday
    """

    calendar_dict: Optional[Dict[str, int]] = _holiday_offset_dict.get(
        calendar
    )
    if calendar_dict is None:
        return None
    return calendar_dict.get(str(datum))
